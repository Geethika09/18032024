package com.qualizeal.javaapp.mainprogram.relational;

public class Relational {
	public void operation1() {
		int x = 30 , y = 10;
		
		if(x==y){
			System.out.println("x equals to y");
		}
		if(x!=y){
			System.out.println("x not equals to y");
		}
		if(x>=y){
			System.out.println("x greater than or equal to y");
		}
		if(x<=y){
			System.out.println("x less than or equal to y");
		}
		if(x<y){
			System.out.println("x less than y");
		}
		if(x>y){
			System.out.println("x greater than y");
		}
	}
}
	